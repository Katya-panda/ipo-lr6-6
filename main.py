import random  # импортируем модуль для генерации случайных чисел
from itertools import combinations  # импортируем функцию для генерации комбинаций
# создаем случайный список из 20 значений, каждое значение — это список из 4 случайных чисел от -10 до 10
random_list = [[random.randint(-10, 10) for _ in range(4)] for _ in range(20)]  
# генерируем все уникальные пары из значений списка, используя кортежи
unique_pairs = list(combinations(random_list, 2))  
# выводим уникальные пары
print(f"Уникальные пары: {unique_pairs}")  
# пользователь вводит целое число
input_number = int(input("Введите целое число: "))  # преобразуем введенное значение в int
# инициализируем счетчик для пар, сумма которых меньше введенного числа
count = 0  
# проходим по каждой паре
for pair in unique_pairs:
    if sum(pair[0]) + sum(pair[1]) < input_number:  # проверяем условие суммы
        count += 1  # если условие выполняется, увеличиваем счетчик
# выводим результат
print(f"Количество пар с суммой меньше {input_number}: {count}")  # выводим количество удовлетворяющих пар
