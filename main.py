import random  # импортируем библиотеку random для генерации случайных чисел
# создаем случайный список из 20 значений, каждый состоит из 4 случайных целых чисел от -10 до 10
random_list = []
for _ in range(20):
    # генерируем 4 случайных целых числа и добавляем их в список
    random_list.append([random.randint(-10, 10) for _ in range(4)])
# печатаем случайный список на экран для визуализации
print("Случайный список:", random_list)
# инициализируем список для хранения уникальных пар
unique_pairs = []
# ищем все уникальные комбинации пар
for i in range(len(random_list)):  # проходим по всем элементам списка
    for j in range(i + 1, len(random_list)):  # начинаем с i+1, чтобы не повторять и не сравнивать с самим собой
        unique_pairs.append((random_list[i], random_list[j]))  # добавляем пару в список уникальных пар
# печатаем уникальные пары для проверки
print("Уникальные пары:", unique_pairs)
# запрашиваем у пользователя ввод целого числа
user_input = int(input("Введите целое число: "))
# инициализируем счётчик для пар, сумма которых меньше введённого числа
count = 0
# проходимся по всем уникальным парам
for pair in unique_pairs:
    # подсчитываем сумму чисел в паре
    pair_sum = sum(pair[0]) + sum(pair[1])
    # проверяем, меньше ли сумма пары введенного числа
    if pair_sum < user_input:
        count += 1  # увеличиваем счётчик, если сумма меньше
# печатаем итоговое количество пар с суммами меньше пользовательского значения
print("Количество пар с суммой меньше", user_input, ":", count)
